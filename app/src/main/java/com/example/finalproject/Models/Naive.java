package com.example.finalproject.Models;

public class Naive {

    private String time;
    private String location;


    public Naive(){}

    public Naive(String time, String location) {
        this.time = time;
        this.location = location;

    }

    public String getTime() {
        return time;
    }

    public String getLocation() {
        return location;
    }



}
