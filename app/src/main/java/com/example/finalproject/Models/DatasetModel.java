package com.example.finalproject.Models;

public class DatasetModel {
    String x;
    String y;

    public DatasetModel(){}

    public DatasetModel(String x, String y) {
        this.x = x;
        this.y = y;
    }

    public String getX() {
        return x;
    }

    public String getY() {
        return y;
    }
}
